const whiteBtn =document.querySelector('.white')
const yellowBtn =document.querySelector('.yellow')
const fusionBtn =document.querySelector('.Fusion')
const blueBtn =document.querySelector('.Blue')
const greenBtn =document.querySelector('.Green')


whiteBtn.addEventListener('click',(e) => document.body.style.backgroundColor ='white' )
yellowBtn.addEventListener('click',(e) =>document.body.style.backgroundColor ='yellow' )
blueBtn.addEventListener('click',(e) => document.body.style.backgroundColor = 'blue')
fusionBtn.addEventListener('click',(e) => document.body.style.backgroundColor ='violet')
greenBtn.addEventListener('click',(e) => document.body.style.backgroundColor ='green')
